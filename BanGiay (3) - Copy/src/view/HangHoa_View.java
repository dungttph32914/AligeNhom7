    public boolean checkTT() {
        boolean check = true;
        if (txtMa.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã");
            check = false;
            return check;
        }         
        else if (txtTen.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên");
            check = false;
            return check;
        } else if (txtLoai.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập loại");
            check = false;
            return check;
        }else if (txtSoluong.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập số lượng");
            check = false;
            return check;
        }else if (txtSize.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập size");
            check = false;
            return check;
        }                           
        else if (Integer.parseInt(txtSoluong.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số dương");
            check = false;
            return check;
        } else if (Integer.parseInt(txtSize.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Size phải là số dương");
            check = false;
            return check;
        }else if (txtMau.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập Màu");
            check = false;
            return check;
        }else {
            return check;
        }
    }
